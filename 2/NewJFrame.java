    private void setupTable() {
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return column < 3;
            }
        };
        
        model.addColumn("Нижний предел");  
        model.addColumn("Верхний предел");   
        model.addColumn("шаг");             
        model.addColumn("Результат");       
        
        tabl.setModel(model);
        
        // Обновление
        model.addTableModelListener(new TableModelListener() {
            @Override
            public void tableChanged(TableModelEvent e) {
                if (e.getType() == TableModelEvent.UPDATE) {
                    int row = e.getFirstRow();
                    int column = e.getColumn();
                    
                    if (row >= 0 && column >= 0 && column < 3 && row < records.size()) {
                        try {
                            double lower = Double.parseDouble(model.getValueAt(row, 0).toString());
                            double upper = Double.parseDouble(model.getValueAt(row, 1).toString());
                            double step = Double.parseDouble(model.getValueAt(row, 2).toString());
                            
                            RecIntegral rec = records.get(row);
                            rec.setLower(lower);
                            rec.setUpper(upper);
                            rec.setStep(step);
                            rec.setResult(0);
                            
                            model.setValueAt("", row, 3);
                        } catch (NumberFormatException ex) {
                        }
                    }
                }
            }
        });
    }
    
