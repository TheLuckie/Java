    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String numberPattern = "-?\\d+(\\.\\d+)?";
    
    if (verhpor.getText().isEmpty() || nizpor.getText().isEmpty() || shag.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Заполните все поля!", "Ошибка", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Проверка на корректность числового формата
    if (!verhpor.getText().matches(numberPattern) || 
        !nizpor.getText().matches(numberPattern) || 
        !shag.getText().matches(numberPattern)) {
        JOptionPane.showMessageDialog(this, "Введите числовые значения!", "Ошибка", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
        double lower = Double.parseDouble(nizpor.getText());
        double upper = Double.parseDouble(verhpor.getText());
        double step = Double.parseDouble(shag.getText());

        if (lower >= upper) {
            JOptionPane.showMessageDialog(this, "Нижний порог должен быть меньше верхнего!", "Ошибка", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (step <= 0) {
            JOptionPane.showMessageDialog(this, "Шаг должен быть положительным числом!", "Ошибка", JOptionPane.ERROR_MESSAGE);
            return;
        }


        RecIntegral record = new RecIntegral(lower, upper, step, 0);
            records.add(record);

        DefaultTableModel tblModel = (DefaultTableModel) tabl.getModel();
        tblModel.addRow(new Object[] {lower, upper, step});
 
        verhpor.setText("");
        nizpor.setText("");
        shag.setText("");

    
    }//GEN-LAST:event_saveActionPerformed

