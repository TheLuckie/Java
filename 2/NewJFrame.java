    private void fillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fillActionPerformed
        // TODO add your handling code here:
         DefaultTableModel model = (DefaultTableModel) tabl.getModel();
        
        model.setRowCount(0);
        
        // Заполняем данными из коллекции
        for (RecIntegral rec : records) {
            String result = rec.getResult() == 0 ? "" : String.format("%.6f", rec.getResult());
            model.addRow(new Object[]{
                rec.getLower(),
                rec.getUpper(),
                rec.getStep(),
                result
            });
        }  
    }//GEN-LAST:event_fillActionPerformed

