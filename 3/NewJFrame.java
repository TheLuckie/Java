    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
       try {
            double lower = Double.parseDouble(nizpor.getText());
            double upper = Double.parseDouble(verhpor.getText());
            double step = Double.parseDouble(shag.getText());
            
            // Проверяем шаг отдельно
            RecIntegral.validateStep(step);
            
            // Создаем запись с проверкой в конструкторе
            RecIntegral record = new RecIntegral(lower, upper, step, 0);
            records.add(record);
            
            DefaultTableModel model = (DefaultTableModel) tabl.getModel();
            model.addRow(new Object[]{lower, upper, step, ""});
            
            nizpor.setText("");
            verhpor.setText("");
            shag.setText("");
            
        } catch (NumberFormatException ex) {
            showError("Введите корректные числа!");
        } catch (CustomException ex) {
            showError(ex.getMessage());
        }
    }//GEN-LAST:event_saveActionPerformed

