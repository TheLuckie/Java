    private void setupTable() {
    DefaultTableModel model = (DefaultTableModel) tabl.getModel();
    
    // Слушатель для обновления коллекции при редактировании таблицы
    model.addTableModelListener((TableModelEvent e) -> {
        if (e.getType() == TableModelEvent.UPDATE) {
            int row = e.getFirstRow();
            int column = e.getColumn();
            
            if (row >= 0 && column >= 0 && column < 3 && row < records.size()) {
                try {
                    double lower = Double.parseDouble(model.getValueAt(row, 0).toString());
                    double upper = Double.parseDouble(model.getValueAt(row, 1).toString());
                    double step = Double.parseDouble(model.getValueAt(row, 2).toString());
                    
                    // Проверяем корректность значений
                    if (step <= 0) {
                        throw new CustomException("Шаг должен быть положительным числом!");
                    }
                    
                    RecIntegral rec = records.get(row);
                    
                    // Обновляем значения с проверкой
                    try {
                        rec.setLower(lower);
                        rec.setUpper(upper);
                        rec.setStep(step);
                        rec.setResult(0);
                        model.setValueAt("", row, 3);
                    } catch (CustomException ex) {
                        // Восстанавливаем старое значение в таблице
                        model.setValueAt(rec.getLower(), row, 0);
                        model.setValueAt(rec.getUpper(), row, 1);
                        model.setValueAt(rec.getStep(), row, 2);
                        showError(ex.getMessage());
                    }
                    
                } catch (NumberFormatException ex) {
                    showError("Ошибка формата числа! Введите корректное число.");
                } catch (CustomException ex) {
                    showError(ex.getMessage());
                }
            }
        }
    });
}

